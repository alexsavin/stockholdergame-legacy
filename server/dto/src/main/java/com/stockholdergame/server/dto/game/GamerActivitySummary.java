package com.stockholdergame.server.dto.game;

import java.util.Date;

/**
 * @author Alexander Savin
 *         Date: 9.6.11 9.30
 */
public class GamerActivitySummary {

    private Date lastSessionStart;

    private Date lastSessionEnd;

    private int gameOffersNumber;

    private int invitationsNumber;

    private int gamesInProgressNumber;

    private int finishedGamesQuantity;

    private int scorersQuantity;

    private int messagesQuantity;

    private int friendsQuantity;

    private int friendRequestsQuantity;

    public Date getLastSessionStart() {
        return lastSessionStart;
    }

    public void setLastSessionStart(Date lastSessionStart) {
        this.lastSessionStart = lastSessionStart;
    }

    public Date getLastSessionEnd() {
        return lastSessionEnd;
    }

    public void setLastSessionEnd(Date lastSessionEnd) {
        this.lastSessionEnd = lastSessionEnd;
    }

    public int getGameOffersNumber() {
        return gameOffersNumber;
    }

    public void setGameOffersNumber(int gameOffersNumber) {
        this.gameOffersNumber = gameOffersNumber;
    }

    public int getInvitationsNumber() {
        return invitationsNumber;
    }

    public void setInvitationsNumber(int invitationsNumber) {
        this.invitationsNumber = invitationsNumber;
    }

    public int getGamesInProgressNumber() {
        return gamesInProgressNumber;
    }

    public void setGamesInProgressNumber(int gamesInProgressNumber) {
        this.gamesInProgressNumber = gamesInProgressNumber;
    }

    public int getFinishedGamesQuantity() {
        return finishedGamesQuantity;
    }

    public void setFinishedGamesQuantity(int finishedGamesQuantity) {
        this.finishedGamesQuantity = finishedGamesQuantity;
    }

    public int getScorersQuantity() {
        return scorersQuantity;
    }

    public void setScorersQuantity(int scorersQuantity) {
        this.scorersQuantity = scorersQuantity;
    }

    public int getMessagesQuantity() {
        return messagesQuantity;
    }

    public void setMessagesQuantity(int messagesQuantity) {
        this.messagesQuantity = messagesQuantity;
    }

    public int getFriendsQuantity() {
        return friendsQuantity;
    }

    public void setFriendsQuantity(int friendsQuantity) {
        this.friendsQuantity = friendsQuantity;
    }

    public int getFriendRequestsQuantity() {
        return friendRequestsQuantity;
    }

    public void setFriendRequestsQuantity(int friendRequestsQuantity) {
        this.friendRequestsQuantity = friendRequestsQuantity;
    }
}
