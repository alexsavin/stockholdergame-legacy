package com.stockholdergame.server.dto.game;

import java.util.List;

/**
 *
 */
public class UserStatisticsList {

    public UserStatisticsList(long totalCount, List<UserStatistics> userStatistics) {
        this.totalCount = totalCount;
        this.userStatistics = userStatistics;
    }

    private long totalCount;

    private List<UserStatistics> userStatistics;

    public long getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(long totalCount) {
        this.totalCount = totalCount;
    }

    public List<UserStatistics> getUserStatistics() {
        return userStatistics;
    }

    public void setUserStatistics(List<UserStatistics> userStatistics) {
        this.userStatistics = userStatistics;
    }
}
