package com.stockholdergame.server.dto.game;

import java.io.Serializable;
import java.util.Date;

/**
 * @author Alexander Savin
 *         Date: 6.3.12 22.06
 */
public class GameEventDto implements Serializable {

    private static final long serialVersionUID = -4837920129279882636L;

    private Long gameId;

    private Integer movesQuantity;

    private String userName;

    private String gameStatus;

    private boolean offer;

    private String eventType;

    private Date created;

    private Object data;

    public GameEventDto() {
    }

    public GameEventDto(Long gameId, String userName) {
        this.gameId = gameId;
        this.userName = userName;
    }

    public Long getGameId() {
        return gameId;
    }

    public void setGameId(Long gameId) {
        this.gameId = gameId;
    }

    public Integer getMovesQuantity() {
        return movesQuantity;
    }

    public void setMovesQuantity(Integer movesQuantity) {
        this.movesQuantity = movesQuantity;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getGameStatus() {
        return gameStatus;
    }

    public void setGameStatus(String gameStatus) {
        this.gameStatus = gameStatus;
    }

    public boolean isOffer() {
        return offer;
    }

    public void setOffer(boolean offer) {
        this.offer = offer;
    }

    public String getEventType() {
        return eventType;
    }

    public void setEventType(String eventType) {
        this.eventType = eventType;
    }

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }
}
