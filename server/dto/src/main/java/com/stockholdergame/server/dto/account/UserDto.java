package com.stockholdergame.server.dto.account;

/**
 * @author Alexander Savin
 *         Date: 18.9.11 11.34
 */
public class UserDto extends UserInfoDto {

    private boolean isRemoved;

    private boolean isOnline;

    private boolean isFriend;

    private boolean isFriendRequestee;

    private boolean isFriendRequestor;

    public boolean isRemoved() {
        return isRemoved;
    }

    public void setRemoved(boolean removed) {
        isRemoved = removed;
    }

    public boolean isOnline() {
        return isOnline;
    }

    public void setOnline(boolean online) {
        isOnline = online;
    }

    public boolean isFriend() {
        return isFriend;
    }

    public void setFriend(boolean friend) {
        isFriend = friend;
    }

    public boolean isFriendRequestee() {
        return isFriendRequestee;
    }

    public void setFriendRequestee(boolean friendRequestee) {
        isFriendRequestee = friendRequestee;
    }

    public boolean isFriendRequestor() {
        return isFriendRequestor;
    }

    public void setFriendRequestor(boolean friendRequestor) {
        isFriendRequestor = friendRequestor;
    }
}
