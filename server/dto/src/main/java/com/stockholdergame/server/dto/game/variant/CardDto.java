package com.stockholdergame.server.dto.game.variant;

import java.util.Set;

/**
 * @author Alexander Savin
 *         Date: 9.10.2010 0.04.38
 */
public class CardDto {

    private Long id;

    private String groupName;

    private Integer quantity;

    private Integer displayOrder;

    private Set<CardOperationDto> cardOperations;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public Integer getDisplayOrder() {
        return displayOrder;
    }

    public void setDisplayOrder(Integer displayOrder) {
        this.displayOrder = displayOrder;
    }

    public Set<CardOperationDto> getCardOperations() {
        return cardOperations;
    }

    public void setCardOperations(Set<CardOperationDto> cardOperations) {
        this.cardOperations = cardOperations;
    }
}
