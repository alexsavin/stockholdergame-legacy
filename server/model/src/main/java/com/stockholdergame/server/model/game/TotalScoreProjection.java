package com.stockholdergame.server.model.game;

/**
 * @author Alexander Savin
 *         Date: 17.6.13 23.32
 */
public class TotalScoreProjection {

    private long totalPlayedGamesCount;

    private long winningsCount;

    private long defeatsCount;

    private long bankruptsCount;

    public TotalScoreProjection(long totalPlayedGamesCount, long winningsCount, long defeatsCount, long bankruptsCount) {
        this.totalPlayedGamesCount = totalPlayedGamesCount;
        this.winningsCount = winningsCount;
        this.defeatsCount = defeatsCount;
        this.bankruptsCount = bankruptsCount;
    }

    public long getTotalPlayedGamesCount() {
        return totalPlayedGamesCount;
    }

    public long getWinningsCount() {
        return winningsCount;
    }

    public long getDefeatsCount() {
        return defeatsCount;
    }

    public long getBankruptsCount() {
        return bankruptsCount;
    }
}
