package com.stockholdergame.server.model.game;

import java.util.Collections;
import java.util.Set;

/**
 * Extra data for game state.
 */
public class GameStateExtraData {

    private Long gameId;

    private GameStatus gameStatus;

    private Long gameVariantId;

    private Set<Long> gamerIds;

    public Long getGameId() {
        return gameId;
    }

    public void setGameId(Long gameId) {
        this.gameId = gameId;
    }

    public GameStatus getGameStatus() {
        return gameStatus;
    }

    public void setGameStatus(GameStatus gameStatus) {
        this.gameStatus = gameStatus;
    }

    public Long getGameVariantId() {
        return gameVariantId;
    }

    public void setGameVariantId(Long gameVariantId) {
        this.gameVariantId = gameVariantId;
    }

    public Set<Long> getGamerIds() {
        return Collections.unmodifiableSet(gamerIds);
    }

    public void setGamerIds(Set<Long> gamerIds) {
        this.gamerIds = gamerIds;
    }
}
